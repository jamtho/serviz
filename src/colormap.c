ColormapType colormap_from_name(const char *name) {
    if (!name) return COLORMAP_VIRIDIS;
    if (strcmp(name, "viridis") == 0) return COLORMAP_VIRIDIS;
    if (strcmp(name, "inferno") == 0) return COLORMAP_INFERNO;
    if (strcmp(name, "plasma") == 0) return COLORMAP_PLASMA;
    if (strcmp(name, "turbo") == 0) return COLORMAP_TURBO;
    return COLORMAP_VIRIDIS;
}

static const ColorRGB *luts[COLORMAP_COUNT] = {
    viridis_lut,
    inferno_lut,
    plasma_lut,
    turbo_lut
};

const ColorRGB *colormap_get_lut(ColormapType type) {
    if (type < 0 || type >= COLORMAP_COUNT) type = COLORMAP_VIRIDIS;
    return luts[type];
}

ColorRGB colormap_sample(ColormapType type, float t) {
    if (t < 0.0f) t = 0.0f;
    if (t > 1.0f) t = 1.0f;
    int idx = (int)(t * 255.0f);
    if (idx < 0) idx = 0;
    if (idx > 255) idx = 255;
    return colormap_get_lut(type)[idx];
}
